/*
Variáveis em JavaScript

Variáveis: Armazenam valores para uso posterior.
- let: Pode ser reatribuído.
- const: Não pode ser reatribuído.
*/

/*
Nomenclatura:
- Use CamelCase para variáveis com múltiplas palavras.
- Pode conter letras, números, _ ou $, mas não pode começar com número.
*/

/*
Boa Prática:
- let para valores mutáveis e const para valores constantes.
*/

