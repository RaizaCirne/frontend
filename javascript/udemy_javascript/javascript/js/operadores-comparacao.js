
let num1 = 3; 
console.log(num1 > 0); // true
console.log(num1 < 0); // false
console.log(num1 >= 3); // true
console.log(num1 <= 3); // true

// === verifica se é estritamente igual/ identico 
console.log(num1 === 3); // true

// !== verifica se é diferente/ não é identico 
console.log(num1 !== 3); // false







